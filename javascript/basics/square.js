// SQUARE
// Problem: Returns the square of the inputted number
// Returns: Number multiplied by itself

const square = (value) => {
  return value * value
}

// Examples:
console.log(square(5)) // 25

// Key concepts:
// - Squaring a number (value * value)
// - Alternative: Math.pow(value, 2) or value ** 2
// - Returns a number

