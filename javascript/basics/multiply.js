// MULTIIPLY
// Problem: Multiplies the two inpputted numbers together
// Returns: Product of two numbers

const multiply = (val1, val2) => {
  return val1 * val2
}

// Examples:
console.log(multiply(1, 2)) // 2
console.log(multiply(2, 2)) // 4

// Key concepts:
// - Multiplication operator (*)
// - Two parameters
// - Returns a number

// When to use:
// - Multiplying two values
// - Scaling values